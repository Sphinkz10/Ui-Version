const fs = require('fs');
const path = require('path');
const apiDir = path.join(__dirname, 'src', 'hooks', 'api');
const targetFile = path.join(__dirname, 'src', 'hooks', 'use-api.ts');
const bckFile = targetFile + '.backup';
const content = fs.readFileSync(fs.existsSync(bckFile) ? bckFile : targetFile, 'utf8');

const regex = /\/\/ ={20,}\r?\n\/\/ (.*?)\r?\n\/\/ ={20,}/g;

let tyStart = content.indexOf('// TYPES');
tyStart = content.lastIndexOf('// ==', tyStart); 
let tyEnd = content.indexOf('// SESSIONS HOOKS');
tyEnd = content.lastIndexOf('// ==', tyEnd);

const coreContentRaw = content.substring(tyStart, tyEnd);
fs.writeFileSync(path.join(apiDir, 'core.ts'), `import apiClient from '@/lib/api-client';\n\n${coreContentRaw}\nexport { fetcher, supabaseApiFetcher };\n`);

let indexFile = `/**\n * Autogenerated API Hooks Index\n * Retro-compatibility layer for ARCH-001\n */\n\nexport * from './api/core';\n`;
const commonImports = `import { useState, useEffect, useCallback } from 'react';\nimport useSWR from 'swr';\nimport apiClient from '@/lib/api-client';\nimport { projectId, publicAnonKey } from '@/utils/supabase/info';\nimport { fetcher, supabaseApiFetcher } from './core';\n\n`;

let hooksContent = content.substring(tyEnd);
let matches = [];
let match;
regex.lastIndex = 0;
while ((match = regex.exec(hooksContent)) !== null) {
  matches.push({ title: match[1].trim(), index: match.index, length: match[0].length });
}

for (let i = 0; i < matches.length; i++) {
  const current = matches[i];
  const next = matches[i + 1];
  const start = current.index + current.length;
  const end = next ? next.index : hooksContent.length;
  let code = hooksContent.substring(start, end).trim();
  if (!code) continue;
  
  let fn = current.title.toLowerCase().replace(/ - .*?$/, '').replace(/ hooks?$/, '').replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
  if (fn === 'custom') fn = 'submissions';
  const fname = `use-${fn}-api.ts`;
  
  fs.writeFileSync(path.join(apiDir, fname), commonImports + code + '\n');
  indexFile += `export * from './api/${fname.replace('.ts', '')}';\n`;
  console.log('Created: ' + fname);
}

fs.writeFileSync(targetFile, indexFile);
console.log('Created core.ts');
console.log('Success version 2!');
